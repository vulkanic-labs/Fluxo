// Group blocks by category
export function getBlocksByCategory() {
  return Object.values(BLOCK_DEFINITIONS).reduce((acc, block) => {
    const cat = block.category;
    if (!acc[cat]) acc[cat] = [];
    acc[cat].push(block);
    return acc;
  }, {} as Record<BlockCategory, BlockDefinition[]>);
}
